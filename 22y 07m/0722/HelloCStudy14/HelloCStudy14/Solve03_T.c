//매개변수 자리에 int numbers를 쓸 경우, -89어쩌고 하는 쓰레기 값을 받음.
//int numbers[] 또는 int* numbers로 써야함.

//일반 함수로 풀이.
int findMinIndex(int numbers[], int size)
{
	int min = numbers[0];
	int min_idx = 0;
	for (int i = 1; i < size; i++)
	{
		if (min > numbers[i])
		{
			min = numbers[i];
			min_idx = i;
		}
	}
	return min_idx;
}

//포인터로 풀이.
int findMinIndex2(int* numbers, int size)
{
	int min = *(numbers);
	int min_idx = 0;
	for (int i = 1; i < size; i++)
	{
		if (min > *(numbers + i))
		{
			min = *(numbers + i);
			min_idx = i;
		}
	}
	return min_idx;
}

